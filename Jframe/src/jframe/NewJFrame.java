    private void btnGenapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenapActionPerformed

        mdl.removeAllElements();
        int awal = Integer.parseInt(txtAwal.getText());
        int akhir = Integer.parseInt(txtAkhir.getText());
        if(awal <= akhir) {
            for(int i=awal; i <=akhir;i++){
                if (i %2==0){
                    mdl.addElement(i);
                }
                
                }
            
            }else {
            for(int i=awal; i>=akhir;i--) {
                if( i %2==0)
                    mdl.addElement(i);
            }
        }
        JList1.setModel(mdl);
    }//GEN-LAST:event_btnGenapActionPerformed

   
